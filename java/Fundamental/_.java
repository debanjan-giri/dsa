
// 1
// JDK (Java Development Kit)
// A software package that includes tools and utilities needed for
// developing Java applications.

// The JDK contains:
// JRE (for running Java applications).
// Java Compiler (converts Java code into bytecode).
// Java Debugger (for debugging code).
// Other tools, like JavaFX SDK and Scene Builder

// 2
// JRE (Java Runtime Environment)
// A software package required to run Java applications on an os

// The JRE contains:
// JVM (Java Virtual Machine): Executes the bytecode.
// Java class libraries and other supporting files.
// It does not include the Java compiler or development tools

// 3
// JVM (Java Virtual Machine)
// The JVM is the engine that executes Java programs.

// Steps:
// The Java compiler converts Java code into bytecode.
// The JVM translates bytecode into machine code for execution.

// Key features of JVM:
// Bytecode is platform-independent,meaning it can run on any os
// Manages memory (via garbage collection).
// Ensures security by verifying and isolating the execution of bytecode.